//Convert an int to double (Widening - Implicit Casting)
//No explicit cast is needed. Java automatically converts int to double.

package JavaPracticeProgram.TypeCastingQue;

public class IntToDoubleTypeCasting {
    public static void main(String[] args) {
        int num = 10;
        double result = num; // Implict Type Casting
        System.out.println("Converted double value: " +result);
    }
}
