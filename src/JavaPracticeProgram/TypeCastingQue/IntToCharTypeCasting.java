//Convert a char to int and vice versa
// Characters are internally stored as ASCII values in Java.




package JavaPracticeProgram.TypeCastingQue;

public class IntToCharTypeCasting {
    public static void main(String[] args) {
        int num = 65;
        char ch = (char)num;
        System.out.println("Character for ASCII 65 :" +ch);
    }
}
