//Convert a char to int and vice versa
// Characters are internally stored as ASCII values in Java.



package JavaPracticeProgram.TypeCastingQue;

public class CharToIntTypeCasting {
    public static void main(String[] args) {
        char ch = 'A';
        int num= ch; //implicit Casting
        System.out.println("ASCII Value of A : " +num);
    }

}
